// Ensure video plays on load (handles some browser autoplay policies)
document.addEventListener('DOMContentLoaded', function() {
    const video = document.getElementById('background-video');
    
    // Force play if not autoplaying
    video.play().catch(function(error) {
        console.log('Autoplay prevented:', error);
        // Optional: Add a play button if needed, but keeping it simple as per request
    });
    
    // Smooth scroll for any future links (not used here but extensible)
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth'
                });
            }
        });
    });
    
    // Add subtle animation on scroll (fade in sections)
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };
    
    const observer = new IntersectionObserver(function(entries) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, observerOptions);
    
    document.querySelectorAll('section').forEach(section => {
        section.style.opacity = '0';
        section.style.transform = 'translateY(20px)';
        section.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
        observer.observe(section);
    });
});
